#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*c;
	size_t	i;

	i = 0;
	c = ft_calloc(ft_strlen(s1) + 1, sizeof(char));
	if (!c)
		return (NULL);
	while (s1[i])
	{
		c[i] = s1[i];
		i++;
	}
	c[i] = '\0';
	return (c);
}
